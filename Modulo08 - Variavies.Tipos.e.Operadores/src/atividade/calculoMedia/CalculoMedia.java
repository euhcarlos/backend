package atividade.calculoMedia;

public class CalculoMedia {

    public static void main(String[] args) {

        double b1 = 4.5d;
        double b2 = 8.0d;
        double b3 = 10.0d;
        double b4 = 3.5d;

        double media = (b1 + b2 + b3 + b4) / 4;

        System.out.println("Media: " + media);

    }

}
